def _default_line_note(number: int, index: int) -> str:
    return (
        f"第{number}卦第{index}爻经典爻辞；"
        "具体解释需结合本卦、变卦、动爻与用户问题语境。"
    )


def _build_hexagram_texts(
    rows: Sequence[RawHexagram],
) -> Dict[int, Dict[str, Any]]:
    texts: Dict[int, Dict[str, Any]] = {}
    for number, theme, judgment, image, lines in rows:
        texts[number] = {
            "theme": theme,
            "judgment": judgment,
            "image": image,
            "lines": {
                index: {
                    "text": text,
                    "note": _default_line_note(number, index),
                }
                for index, text in zip(LINE_INDEXES, lines)
            },
        }
    return texts


HEXAGRAM_TEXTS: Dict[int, Dict[str, Any]] = _build_hexagram_texts(RAW_HEXAGRAM_TEXTS)


def validate_hexagram_texts() -> List[str]:
    """Return human-readable validation issues for the bundled text corpus."""

    issues: List[str] = []
    expected_numbers = set(range(1, 65))
    actual_numbers = set(HEXAGRAM_TEXTS)

    for number in sorted(expected_numbers - actual_numbers):
        issues.append(f"missing_hexagram:{number}")
    for number in sorted(actual_numbers - expected_numbers):
        issues.append(f"unexpected_hexagram:{number}")

    for number in sorted(expected_numbers & actual_numbers):
        data = HEXAGRAM_TEXTS[number]
        for key in ("theme", "judgment", "image", "lines"):
            if key not in data:
                issues.append(f"hexagram:{number}:missing_key:{key}")
        if not str(data.get("theme", "")).strip():
            issues.append(f"hexagram:{number}:empty_theme")
        if not str(data.get("judgment", "")).strip():
            issues.append(f"hexagram:{number}:empty_judgment")
        if not str(data.get("image", "")).strip():
            issues.append(f"hexagram:{number}:empty_image")

        lines = data.get("lines", {})
        if set(lines) != set(LINE_INDEXES):
            issues.append(f"hexagram:{number}:invalid_line_indexes")
        for index in LINE_INDEXES:
            line = lines.get(index) or {}
            if not str(line.get("text", "")).strip():
                issues.append(f"hexagram:{number}:line:{index}:empty_text")
            if not str(line.get("note", "")).strip():
                issues.append(f"hexagram:{number}:line:{index}:empty_note")

    return issues


def get_hexagram_text(number: int) -> Optional[Dict[str, Any]]:
    """Return text metadata for one King Wen hexagram number, if available."""

    data = HEXAGRAM_TEXTS.get(number)
    if data is None:
        return None
    result = deepcopy(data)
    result["source"] = TEXT_SOURCE
    result["source_url"] = TEXT_SOURCE_URL
    result["coverage"] = TEXT_COVERAGE
    return result


def get_line_text(number: int, line_index: int) -> Optional[Dict[str, Any]]:
    """Return line text for a hexagram and 1-based line index, if available."""

    data = HEXAGRAM_TEXTS.get(number)
    if data is None:
        return None
    line = data.get("lines", {}).get(line_index)
    if line is None:
        return None
    result = deepcopy(line)
    result["source"] = TEXT_SOURCE
    result["source_url"] = TEXT_SOURCE_URL
    return result


def has_hexagram_text(number: int) -> bool:
    """Return whether the curated text corpus currently covers a hexagram."""

    return number in HEXAGRAM_TEXTS


